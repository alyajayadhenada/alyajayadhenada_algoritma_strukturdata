nilai = 75  # Tentukan nilai terlebih dahulu

if nilai >= 90:
    print('nilai anda adalah A')

elif nilai >= 80:
    print('nilai anda adalah B')

elif nilai >= 60:
    print('nilai anda adalah C')

elif nilai >= 40:
    print('nilai anda adalah D')

else:
    print('nilai anda adalah E')  # Menambahkan nilai E untuk nilai kurang dari 40

